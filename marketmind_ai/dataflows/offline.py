def get_insider_transactions(symbol: str, analysis_date: str) -> str:
    meta = _lookup(symbol)
    if float(meta["revenue_growth"]) > 0.2:
        return "Recent insider activity does not obviously contradict the growth narrative; no large distress pattern is visible."
    return "Insider context is limited and does not provide a strong confirming signal."


def get_fundamental_documents(symbol: str, curr_date: str) -> list[dict]:
    from .fundamentals_rag import chunk_document_text

    meta = _lookup(symbol)
    sections = [
        (
            "10-K",
            f"{meta['name']} annual filing",
            (
                "Item 1 Business\n"
                f"{meta['description']} Revenue growth currently tracks {meta['revenue_growth']:.2f} with "
                f"gross margin near {meta['gross_margin']:.2f}. The business mix emphasizes durable customer demand, "
                "execution discipline, and competitive positioning.\n\n"
                "Item 7 Management Discussion and Analysis\n"
                f"Operating margin is near {meta['operating_margin']:.2f}, debt-to-equity is {meta['debt_to_equity']:.2f}, "
                f"and current ratio is {meta['current_ratio']:.2f}. Capital allocation remains anchored by free cash flow of "
                f"{meta['free_cashflow']:.0f} and management continues to frame guidance around backlog, demand visibility, and margins."
            ),
        ),
        (
            "earnings_call_transcript",
            f"{meta['name']} latest earnings call transcript",
            (
                "CEO: Demand trends remained resilient and management highlighted customer expansion, revenue visibility, "
                "and disciplined execution.\n"
                "CFO: Margin structure stayed healthy, capital allocation remained selective, and liquidity continued to "
                "support product investment and balance-sheet resilience."
            ),
        ),
    ]
    snippets: list[dict] = []
    for doc_type, title, text in sections:
        snippets.extend(
            chunk_document_text(
                text,
                title=title,
                doc_type=doc_type,
                filing_date=curr_date,
                provider="offline-fixtures",
                source=f"offline-fixtures:{symbol.upper()}:{doc_type}",
            )
        )
    return snippets


def validate_price_data(symbol: str, analysis_date: str) -> bool:
    try:
        return len(get_price_history(symbol, analysis_date, 90)) >= 20
    except Exception:
        return False


def validate_fundamental_data(symbol: str, analysis_date: str) -> bool:
    try:
        return bool(get_fundamentals(symbol).company_name)
    except Exception:
        return False
