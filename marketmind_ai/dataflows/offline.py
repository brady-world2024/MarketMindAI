def get_news(query: str, analysis_date: str, limit: int = 8) -> list[NewsItem]:
    symbol = query.upper().split()[0]
    meta = _lookup(symbol)
    anchor = datetime.strptime(analysis_date, "%Y-%m-%d").date()
    items: list[NewsItem] = []
    for index, (title, score) in enumerate(meta["news"][:limit]):
        published = anchor - timedelta(days=index * 3 + 1)
        items.append(
            NewsItem(
                title=title,
                source="Offline Desk",
                published_at=published.isoformat(),
                url="",
                summary=title,
                sentiment_score=score,
            )
        )
    return items


def get_global_news(analysis_date: str, theme: str = "macro", limit: int = 8) -> list[NewsItem]:
    headlines = [
        ("Rates and liquidity remain central to growth-stock leadership.", 0.05),
        ("AI capex continues to dominate enterprise and cloud spending debates.", 0.12),
        ("Broad market breadth and funding conditions remain the key macro cross-check.", -0.02),
    ]
    anchor = datetime.strptime(analysis_date, "%Y-%m-%d").date()
    items = []
    for index, (title, score) in enumerate(headlines[:limit]):
        items.append(
            NewsItem(
                title=f"{theme.title()}: {title}",
                source="Offline Macro Desk",
                published_at=(anchor - timedelta(days=index + 1)).isoformat(),
                summary=title,
                sentiment_score=score,
            )
        )
    return items


def get_indicators(symbol: str, analysis_date: str, indicators: list[str] | None = None) -> str:
    bars = get_price_history(symbol, analysis_date, 260)
    return render_indicator_report(bars, indicators)


