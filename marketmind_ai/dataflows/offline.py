def search_symbols(query: str, limit: int = 8) -> list[SymbolCandidate]:
    cleaned = " ".join(query.strip().split()).lower()
    if not cleaned:
        return []
    candidates: list[SymbolCandidate] = []
    for symbol, meta in _FIXTURES.items():
        if cleaned in symbol.lower() or cleaned in meta["name"].lower():
            candidates.append(
                SymbolCandidate(
                    symbol=symbol,
                    name=meta["name"],
                    exchange=meta["exchange"],
                    region=meta["region"],
                    currency=meta["currency"],
                    instrument_type="Equity",
                    provider="offline",
                    match_score=0.82 if cleaned != symbol.lower() else 0.99,
                )
            )
    if cleaned.upper() not in _FIXTURES and not candidates and cleaned.isalpha():
        symbol = cleaned.upper()
        meta = _lookup(symbol)
        candidates.append(
            SymbolCandidate(
                symbol=symbol,
                name=meta["name"],
                exchange=meta["exchange"],
                region=meta["region"],
                currency=meta["currency"],
                instrument_type="Equity",
                provider="offline",
                match_score=0.58,
            )
        )
    return candidates[:limit]


def get_price_history(symbol: str, analysis_date: str, lookback_days: int = 180) -> list[PriceBar]:
    meta = _lookup(symbol)
    anchor = datetime.strptime(analysis_date, "%Y-%m-%d").date()
    days = weekday_series(anchor, lookback_days)
    seed = sum(ord(ch) for ch in symbol.upper()) + int(anchor.strftime("%Y%m%d"))
    rng = random.Random(seed)
    close = float(meta["base_price"]) * (0.92 + rng.random() * 0.12)
    bars: list[PriceBar] = []
    for index, day in enumerate(days):
        drift = float(meta["trend_bias"]) / 252.0
        shock = rng.uniform(-float(meta["volatility"]), float(meta["volatility"]))
        close = max(2.0, close * (1.0 + drift + shock))
        open_price = max(1.0, close * (1.0 + rng.uniform(-0.007, 0.007)))
        high = max(open_price, close) * (1.0 + abs(rng.uniform(0.0, 0.015)))
        low = min(open_price, close) * (1.0 - abs(rng.uniform(0.0, 0.015)))
        volume = int(int(meta["volume"]) * (0.82 + rng.random() * 0.36))
        if index > len(days) - 15 and float(meta["trend_bias"]) > 0:
            close *= 1.002
        bars.append(
            PriceBar(
                date=day.isoformat(),
                open=round(open_price, 2),
                high=round(high, 2),
                low=round(low, 2),
                close=round(close, 2),
                volume=volume,
            )
        )
    return bars


